package com.ternaryoperator;

public class TernaryOperator {

	public static void main(String[] args) {

		int n1, n2, r;
		n1 = 14;
		n2 = 8;
		r = n1 < n2 ? 0 : 1; /* primeiro é realizada a condição, logo após a interrogação, o que tiver antes dos dois pontos é o
								resultado caso a condição seja verdadeira, e depois dos dois pontos o resultado
								caso a condição seja falsa seja falsa */
		System.out.println(r);
		
		
	}

}
